# frozen_string_literal: true

module RmsWebService
  module Response
    module Item
      class Search < Parser
        attr_accessor :code
        def initialize(xml)
          super
          @parsed_xml.xpath("//items").children.each { |item| self << Get.new(item.to_xml) }
        end

        def success?
          @code == "200-00" ? true : false
        end
      end
    end
  end
end
